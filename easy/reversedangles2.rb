=begin 

input = a method that takes array as argument

output = a NEW reversed array elements in the same array 
passed into the method, NOT THE SAME object id!

ex: list = [1,2,3,4]
result = reverse!(list)
result == [4, 3, 2, 1]
list == [4, 3, 2, 1]
list.object_id == result.object_id

=end

=begin 

def reverse(arr)
  right_index = -1
  new_arr = []

  arr.size.times do 
    new_arr << arr[right_index]
    right_index -= 1
  end
  new_arr
end

p reverse([1,2,3,4]) == [4,3,2,1]          # => true
p reverse(%w(a b e d c)) == %w(c d e b a)  # => true
p reverse(['abc']) == ['abc']              # => true
p reverse([]) == []                        # => true

p list = [1, 3, 2]                      # => [1, 3, 2]
p new_list = reverse(list)              # => [2, 3, 1]
p list.object_id != new_list.object_id  # => true
p list == [1, 3, 2]                     # => true
p new_list == [2, 3, 1]  

=end

